char	*ft_strcpy(char *s1, char *s2);

char	*ft_strcpy(char *s1, char *s2)
{
	char *pt_st1;

	pt_st1 = s1;
	while (*s2)
	{
		*s1 = *s2;
		s2++;
		s1++;
	}
	*s1 = '\0';
	return (pt_st1);
}
