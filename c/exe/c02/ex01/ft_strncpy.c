char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*pt_dest;
	unsigned int	i;

	i = 0;
	pt_dest = dest;
	while (src[i] && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
		dest[i++] = 0;
	return (pt_dest);
}
